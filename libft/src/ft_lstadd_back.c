#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!*lst)
	{
		ft_lstadd_front(lst, new);
		return ;
	}
	tmp = ft_lstlast(*lst);
	tmp->next = new;
}
